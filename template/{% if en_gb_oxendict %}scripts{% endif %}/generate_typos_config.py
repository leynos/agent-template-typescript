"""Generate ``typos.toml`` enforcing en-GB-oxendict (Oxford) spelling.

The ``en-gb`` locale in ``typos`` corrects American spellings to British
ones, but it prefers the ``-ise`` family and rejects Oxford ``-ize`` forms.
For each curated stem this script therefore emits:

- an identity entry accepting each Oxford ``-ize`` inflection that the
  locale would otherwise "correct" to ``-ise``, and
- an ``-ise`` to ``-ize`` correction for the same inflection, which the
  locale accepts silently.

Stems taking ``-yse`` (analyse, paralyse, catalyse) are deliberately
absent: Oxford retains ``-yse`` and the locale already enforces it.
Genuinely ``-ise``-only words (advise, revise, exercise, supervise, and so
on) are excluded from the stem list, so they remain accepted.

Examples
--------
Run from the repository root to rewrite ``typos.toml`` in place:

>>> # uv run scripts/generate_typos_config.py  # doctest: +SKIP

Pass ``--check`` to verify the committed file instead of rewriting it: the
rendered configuration must parse as TOML with no duplicate keys, contain
the expected number of ``extend-words`` entries, and match the committed
``typos.toml`` byte for byte. A non-zero exit reports drift:

>>> # uv run scripts/generate_typos_config.py --check  # doctest: +SKIP

The stem ``organ`` produces entries such as ``organised = "organized"``
and ``organized = "organized"`` for every suffix pair.
"""

